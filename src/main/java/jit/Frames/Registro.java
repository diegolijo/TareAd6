    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nome = jTextField1.getText();
        String user = jTextField2.getText();

        char[] charPass = jPasswordField1.getPassword();
        String pass = "";
        for (int i = 0; i < charPass.length; i++) {
            pass += charPass[i];
        }

        // comprobamos campos
        if (!nome.equals("") && !user.equals("") && !pass.equals("")) {

            BaseDatos bd = new BaseDatos();
            DBCollection colecion = bd.getBaseDatos().getCollection("usuario");
            DBObject query = new BasicDBObject("username", user);
            DBObject cursor = colecion.findOne(query);

            if (cursor == null) {

                //insertar usuario
                DBObject novoUsuario = new BasicDBObject()
                        .append("nome", nome)
                        .append("username", user)
                        .append("password", pass);
                colecion.insert(novoUsuario);

                JOptionPane.showMessageDialog(null, "registro ok", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                login.setJTextfielUser(user);
                 
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "O username xa estÃ¡ rexistrado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

